using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ParserSim
{
    class Point3D
    {
        public double x = 0;
        public double y = 0;
        public double z = 0;

        public Point3D()
        {

        }

        public Point3D(double x, double y, double z)
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}
